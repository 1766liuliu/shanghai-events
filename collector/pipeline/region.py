"""地域过滤 —— 只保留上海的活动,剔除漏进来的外地展会/巡演站。

难点:上海有大量"省市名命名的马路"(南京路、西藏路、云南路、北京西路…),
  不能因为标题/场馆出现"南京/西藏/云南"就误删。
规则:
  1) 命中"上海信号"(上海/上海地标/上海场馆)→ 一律保留;
  2) 否则,若出现"外地省市名 且 其后不是'路'"(即不是上海马路)→ 判为外地,丢弃;
  3) 都不命中(没提任何地名)→ 默认保留(来自上海源)。
"""
from __future__ import annotations

import re
from typing import List

from models import Event

# 上海信号(命中任一即视为上海,优先级最高)
SH_SIGNALS = [
    "上海", "外滩", "浦东", "浦西", "静安", "徐汇", "虹口", "杨浦", "黄浦",
    "闵行", "宝山", "嘉定", "松江", "青浦", "奉贤", "金山", "临港", "虹桥",
    "世博", "陆家嘴", "人民广场", "新天地", "豫园", "迪士尼", "旗忠",
    "梅赛德斯", "奔驰文化中心", "东方体育", "虹口足球场", "国家会展中心",
    "新国际博览", "世博展览馆", "上海展览中心",
]

# 外地省市 / 港澳台(出现且非"XX路"且无上海信号 → 判为外地)
OTHER_PLACES = [
    # 直辖市/主要城市
    "北京", "广州", "深圳", "成都", "杭州", "南京", "武汉", "重庆", "宁波",
    "青岛", "天津", "西安", "苏州", "长沙", "郑州", "合肥", "沈阳", "大连",
    "无锡", "东莞", "佛山", "厦门", "济南", "昆明", "哈尔滨", "福州", "南昌",
    "贵阳", "太原", "石家庄", "烟台", "温州", "珠海", "海口", "三亚", "长春",
    # 省 / 自治区
    "山东", "广东", "江苏", "浙江", "河北", "河南", "湖北", "湖南", "四川",
    "福建", "安徽", "江西", "辽宁", "陕西", "山西", "云南", "贵州", "广西",
    "甘肃", "海南", "吉林", "黑龙江", "内蒙古", "新疆", "宁夏", "青海",
    # 港澳台
    "香港", "澳门", "台湾", "台北", "高雄",
]
# 外地名后跟"路/弄/街/桥"等 → 视为上海马路名,不算外地(.{0,1} 兼顾 东路/南路 等)
_PATTERNS = [re.compile(p + r"(?!.{0,1}路)(?!.{0,1}弄)(?!街)(?!大道)(?!高架)(?!立交)")
             for p in OTHER_PLACES]


def filter_shanghai(events: List[Event]) -> List[Event]:
    out: List[Event] = []
    dropped = 0
    for e in events:
        text = f"{e.title} {e.venue}"
        if any(s in text for s in SH_SIGNALS):
            out.append(e)
            continue
        if any(p.search(text) for p in _PATTERNS):
            dropped += 1
            continue
        out.append(e)
    print(f"[region] 剔除外地活动 {dropped} 条,保留 {len(out)} 条")
    return out

"""地域过滤(北京版) —— 只保留北京的活动,剔除漏进来的外地展会/巡演站。

与 region.py(上海版)结构完全对称,独立成文件,避免任何共享状态导致
两城市数据在过滤这一步产生交叉。规则同源:
  1) 命中"北京信号"(北京/北京地标/北京场馆)→ 一律保留;
  2) 否则,若出现"外地省市名(含上海)且其后不是'路'"→ 判为外地,丢弃;
  3) 都不命中(没提任何地名)→ 默认保留(来自北京源)。
"""
from __future__ import annotations

import re
from typing import List

from models import Event

# 北京信号(命中任一即视为北京,优先级最高)
BJ_SIGNALS = [
    "北京", "东城", "西城", "朝阳", "海淀", "丰台", "石景山", "通州", "顺义",
    "昌平", "大兴", "房山", "门头沟", "怀柔", "平谷", "密云", "延庆",
    "天安门", "王府井", "西长安街", "五棵松", "798", "大栅栏", "国贸", "三里屯",
    "鸟巢", "水立方", "工体", "工人体育场", "国家体育场", "国家会议中心",
    "首都", "中关村", "望京", "亦庄",
]

# 外地省市 / 港澳台(含上海;出现且非"XX路"且无北京信号 → 判为外地)
OTHER_PLACES = [
    "上海", "广州", "深圳", "成都", "杭州", "南京", "武汉", "重庆", "宁波",
    "青岛", "天津", "西安", "苏州", "长沙", "郑州", "合肥", "沈阳", "大连",
    "无锡", "东莞", "佛山", "厦门", "济南", "昆明", "哈尔滨", "福州", "南昌",
    "贵阳", "太原", "石家庄", "烟台", "温州", "珠海", "海口", "三亚", "长春",
    "山东", "广东", "江苏", "浙江", "河北", "河南", "湖北", "湖南", "四川",
    "福建", "安徽", "江西", "辽宁", "陕西", "山西", "云南", "贵州", "广西",
    "甘肃", "海南", "吉林", "黑龙江", "内蒙古", "新疆", "宁夏", "青海",
    "香港", "澳门", "台湾", "台北", "高雄",
]
_PATTERNS = [re.compile(p + r"(?!.{0,1}路)(?!.{0,1}弄)(?!街)(?!大道)(?!高架)(?!立交)")
             for p in OTHER_PLACES]


def filter_beijing(events: List[Event]) -> List[Event]:
    out: List[Event] = []
    dropped = 0
    for e in events:
        text = f"{e.title} {e.venue}"
        if any(s in text for s in BJ_SIGNALS):
            out.append(e)
            continue
        if any(p.search(text) for p in _PATTERNS):
            dropped += 1
            continue
        out.append(e)
    print(f"[region_beijing] 剔除外地活动 {dropped} 条,保留 {len(out)} 条")
    return out
